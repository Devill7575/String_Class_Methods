class IndexOfDemo{
        public static void main(String args[]) {
                String str = "shashi";
                System.out.println(str.indexOf('s',0));
                System.out.println(str.indexOf('s',1));
                System.out.println(str.indexOf('s',2));
                System.out.println(str.indexOf('h',0));
                System.out.println(str.indexOf('h',3));
                System.out.println(str.indexOf('h',4));
        }
}