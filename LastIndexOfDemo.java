class LastIndexOfDemo{
        public static void main(String args[]) {
                String str = "abcde";
                System.out.println(str.lastIndexOf('e',0));
                System.out.println(str.lastIndexOf('a',0));
                System.out.println(str.lastIndexOf('e',4));
                System.out.println(str.lastIndexOf('a',4));
        }
}